package it.polito.tdp.model;

public class Distanze {
	
	private double lat;
	private double lon;
	
	public Distanze(double lat, double lon) {
		super();
		this.lat = lat;
		this.lon = lon;
	}
	public double getLat() {
		return lat;
	}
	public double getLon() {
		return lon;
	}
	
	

}
